     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $mas = new Masyarakat();

        $mas->nik = $request->nik;
        $mas->nama = $request->nama;
        $mas->username = $request->username;
        $mas->telp = $request->telp;
        $mas->password = bcrypt($request->password);

        $mas->save();

        return redirect()->route('masyarakat.login.post')->with('success', 'Berhasil Registrasi, Silahkan Login');
    }

