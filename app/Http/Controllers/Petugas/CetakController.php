    public function cetakData($tglawal, $tglakhir)
    {
        $cetakLaporan = Pengaduan::whereBetween('tgl_pengaduan', [$tglawal, $tglakhir])->where('status', 'selesai')->get();
        $data['cetakLaporan'] = $cetakLaporan;

        // dd($data);
        return view('petugas.cetak.cetak', $data);
